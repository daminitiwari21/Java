public class ComplexPattern {
    public static void main(String[] args) {
    
        for (int i = 0; i < 2; i++) {
            System.out.println("*********************");
        }
        for (int i = 0; i < 2; i++) {
            System.out.println("***     ******     **");
        }

        for (int i = 0; i < 2; i++) {
            System.out.println("*********************");
        }

        for (int i = 0; i < 2; i++) {
            System.out.println("*****          ******");
        }

        for (int i = 0; i < 2; i++) {
            System.out.println("*********************");
        }
    }
}
